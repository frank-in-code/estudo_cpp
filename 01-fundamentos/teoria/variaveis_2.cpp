#include <iostream> 
using namespace std;

int main() {
    // Também é possível alterar o valor da variável ao longo do programa
    int numero;      // Declara a variável 'numero' sem valor inicial

    numero = 20;     // Atribui o valor 20 à variável 'numero'

    cout << numero << endl;  // Imprime 20 na tela

    numero = 40;     // Modifica o valor da variável 'numero' para 40

    cout << numero << endl;  // Imprime 40 na tela

    int outronumero = 30;    // Declara e inicializa a variável 'outronumero' com 30

    cout << outronumero << endl;  // Imprime 30 na tela

    return 0;
}
