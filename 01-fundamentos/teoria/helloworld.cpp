#include <iostream> 
// incluir a biblioteca iostream (entrada e saída de dados)

// Comentários ajudam a organizar e explicar o código (//)
// Eles não são executados pelo programa

int main() {
    // main é a função principal do programa
    // int indica que ela retorna um valor inteiro (0 = sucesso)

    // std::cout envia dados para a saída padrão (console)
    // << é o operador de inserção, usado para enviar dados
    std::cout << "Hello World!" << std::endl; // imprime a mensagem na tela

    return 0; // retorna 0 para o sistema operacional, indicando que terminou corretamente
}
